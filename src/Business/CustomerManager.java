package Business;

import Business.Interfaces.ICustomerManager;

public class CustomerManager implements ICustomerManager {
    private int id;
    private String name;

    public CustomerManager(int id, String name) {
        this.id = id;
        this.name = name;
    }

    @Override
    public int getId() {
        return id;
    }

    @Override
    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String getName() {
        return name;
    }

    @Override
    public void setName(String name) {
        this.name = name;
    }
}
